// Program to demostrate the use of static array

public class Array1 {
    public static void main(String[] args) {
        //        0 1  2  3  4
        int a[]={10,20,30,40,50};       //integer arrays
        char x[]={'H','e','l','l','o'}; //character array
        for (int i=0; i<5;i++){
            System.out.println(a[i]);
        }
        for (int i=0; i<5;i++){
            System.out.println(x[i]);
        }
    }
    
}
